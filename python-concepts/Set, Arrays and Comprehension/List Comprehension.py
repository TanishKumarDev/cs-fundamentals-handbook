arr = [1, 2, 3, 4, 5]
res = [i*2 for i in arr] # Create a new list with the elements of the original list multiplied by 2
print(res)

# Using For loop
res = [] # Create a new list
for i in arr:
    res.append(i*2) # Append the elements of the original list multiplied by 2 to the new list
print(res)
